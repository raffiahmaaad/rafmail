// Function to generate email username
export function generateEmailUsername(): string {
  // Randomly select country
  const countries = ["indonesia", "usa", "uk", "spain", "germany", "france"];
  const country = countries[Math.floor(Math.random() * countries.length)];
  
  let firstName: string;
  let lastName: string;
  
  switch (country) {
    case "indonesia":
      firstName = INDONESIAN_FIRST_NAMES[Math.floor(Math.random() * INDONESIAN_FIRST_NAMES.length)];
      lastName = INDONESIAN_LAST_NAMES[Math.floor(Math.random() * INDONESIAN_LAST_NAMES.length)];
      break;
    case "usa":
      firstName = AMERICAN_FIRST_NAMES[Math.floor(Math.random() * AMERICAN_FIRST_NAMES.length)];
      lastName = AMERICAN_LAST_NAMES[Math.floor(Math.random() * AMERICAN_LAST_NAMES.length)];
      break;
    case "uk":
      firstName = BRITISH_FIRST_NAMES[Math.floor(Math.random() * BRITISH_FIRST_NAMES.length)];
      lastName = BRITISH_LAST_NAMES[Math.floor(Math.random() * BRITISH_LAST_NAMES.length)];
      break;
    case "spain":
      firstName = SPANISH_FIRST_NAMES[Math.floor(Math.random() * SPANISH_FIRST_NAMES.length)];
      lastName = SPANISH_LAST_NAMES[Math.floor(Math.random() * SPANISH_LAST_NAMES.length)];
      break;
    case "germany":
      firstName = GERMAN_FIRST_NAMES[Math.floor(Math.random() * GERMAN_FIRST_NAMES.length)];
      lastName = GERMAN_LAST_NAMES[Math.floor(Math.random() * GERMAN_LAST_NAMES.length)];
      break;
    case "france":
      firstName = FRENCH_FIRST_NAMES[Math.floor(Math.random() * FRENCH_FIRST_NAMES.length)];
      lastName = FRENCH_LAST_NAMES[Math.floor(Math.random() * FRENCH_LAST_NAMES.length)];
      break;
    default:
      firstName = INDONESIAN_FIRST_NAMES[Math.floor(Math.random() * INDONESIAN_FIRST_NAMES.length)];
      lastName = INDONESIAN_LAST_NAMES[Math.floor(Math.random() * INDONESIAN_LAST_NAMES.length)];
  }
  
  // Generate random number (100-9999)
  const randomNum = Math.floor(Math.random() * 9900) + 100;
  
  // Format: firstname.lastname+randomNumber
  return `${firstName}.${lastName}${randomNum}`;
}
